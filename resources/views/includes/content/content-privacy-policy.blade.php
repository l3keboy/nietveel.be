<p id="cookie" class="data-p-2-title">Cookies</p>
<p class="data-p-2-m1">
    De NietVeel.be site maakt gebruiik van 'cookies'. Een cookie is een klein gegevensbestand dat via uw
    webbrowser naar uw lokale schijf wordt overgebracht en alleen kan worden gelezen door de website die
    de cookie op uw lokale schijf heeft geplaatst. De cookies die geplaatst worden, worden gebruikt voor
    de functionaliteit van de website, denk aan het onthouden van de themavoorkeuren of het gebruik van <a id="#recaptcha-link" href="#recaptcha">reCAPTCHA</a>.
</p>

<p id="recaptcha" class="data-p-3-title">reCAPTCHA</p>
<p class="data-p-3-m1">
    We gebruiken de reCAPTCHA service van Google Inc. (Google) om gegevens te beschermen die verstuurd
    worden via deze website. Die functie controleert of een bericht wel door een persoon wordt verstuurd, om
    zo te voorkomen dat ‘spam bots’ bijvoorbeeld automatisch berichten sturen of commentaren achterlaten.
    Hierbij worden het IP adres van een bezoeker en mogelijk ook andere data verstuurd om Google reCAPTCHA
    te laten werken. De inhoud van een bericht wordt hiervoor verstuurd naar en verwerkt door Google. Het IP
    adres wordt hierbij afgekort en daardoor anoniem gemaakt in landen die deel uitmaken van de EU en staten
    die deelnemen aan het verdrag voor het Europese economische gebied. In uitzonderingsgevallen wordt een
    IP adres volledig doorgegeven aan een Google server in de Verenigde Staten en daar afgekort. Namens de
    bezitter van deze website gebruikt Google deze gegevens om je gebruik van deze dienst te evalueren. Het
    IP adres dat reCAPTCHA doorgeeft, wordt niet samengevoegd met andere data van Google. Op het verzamelen
    van deze gegevens zijn Google’s regels voor gegevensbescherming van toepassing. Meer informatie over
    over Google’s privacybeleid vind je op deze pagina:
    <a class="google-privacy" href="https://www.google.com/intl/nl/policies/privacy/">
        https://www.google.com/intl/nl/policies/privacy/</a>.
    Door reCAPTCHA te gebruiken ga je ermee akkoord dat Google gegevens over jou verwerkt op de manier en
    voor het doel zoals hierboven beschreven.
</p>
